public class Calculadora {
	
public int valor;
	
	public double somar(double a, double b) {
		return a + b;
	}
	
	public double subitrair(double a, double b) {
		return a - b;
	}
	
	public double multiplicar(double a, double b) {
		return a * b;
	}
	
	public double dividir(double a, double b) {
		return a / b;
	}

}
